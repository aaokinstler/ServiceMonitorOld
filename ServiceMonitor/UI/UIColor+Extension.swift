import UIKit

extension UIColor {
    
    public class var customGreen: UIColor {
        return UIColor (displayP3Red: 97/255, green: 195/255, blue: 83/255, alpha: 1.0)
    }
    
    public class var customYellow: UIColor {
        return UIColor(displayP3Red: 244/255, green: 191/255, blue: 79/255, alpha: 1.0)
    }
    
    public class var customRed: UIColor {
        return UIColor(displayP3Red: 237/255, green: 106/255, blue: 94/255, alpha: 1.0)
    }
}
